# Versioned models: a `versions:` block + per-version `include`/`exclude`
# ---------------------------------------------------------------------------


def _ensure_versions_block(entry: dict) -> CommentedSeq:
    versions = entry.get("versions")
    if not isinstance(versions, list):
        versions = CommentedSeq()
        entry["versions"] = versions
    return versions


def _find_bullet(versions: CommentedSeq, v: str) -> Optional[dict]:
    for bullet in versions:
        if isinstance(bullet, dict) and str(bullet.get("v")) == str(v):
            return bullet
    return None


def _ensure_version_bullet(versions: CommentedSeq, v: str, defined_in: Optional[str] = None) -> dict:
    bullet = _find_bullet(versions, v)
    if bullet is None:
        bullet = CommentedMap()
        bullet["v"] = int(v)
        if defined_in is not None:
            bullet["defined_in"] = defined_in  # file isn't dbt's default `<model>_v<N>.sql`
        versions.append(bullet)
    return bullet


def _set_latest_version(entry: dict, versions: CommentedSeq) -> None:
    """Point `latest_version` at the highest declared version. Always recomputed, so the result is
    independent of sync order (a user who wants an older `latest_version` sets it outside sync)."""
    declared = [str(b.get("v")) for b in versions if isinstance(b, dict) and b.get("v") is not None]
    if not declared:
        return
    numeric = [d for d in declared if d.isdigit()]
    top = max(numeric, key=int) if numeric else sorted(declared)[-1]
    entry["latest_version"] = int(top) if top.isdigit() else top


def _bullet_include_element(bullet: dict) -> dict:
    """The single `{include, exclude}` element on a bullet, created as `include: '*'` if absent."""
    cols = bullet.get("columns")
    if not isinstance(cols, list):
        cols = CommentedSeq()
        bullet["columns"] = cols
    for element in cols:
        if isinstance(element, dict) and "include" in element:
            return element
    element = CommentedMap()
    element["include"] = "*"
    cols.insert(0, element)
    return element


def _bullet_set_exclude(bullet: dict, names: list[str]) -> None:
    """Set the bullet's `exclude` to `names` (ordered). Empty → drop it, and collapse a now-bare
    `columns: [{include: '*'}]` (no overrides) back to no `columns` key."""
    if names:
        element = _bullet_include_element(bullet)
        element["exclude"] = CommentedSeq(names)
        return
    cols = bullet.get("columns")
    if not isinstance(cols, list):
        return
    for element in cols:
        if isinstance(element, dict) and "include" in element:
            element.pop("exclude", None)
    if not any(isinstance(c, dict) and "name" in c for c in cols) and all(
        isinstance(c, dict) and set(c.keys()) <= {"include"} and c.get("include") in ("*", "all") for c in cols
    ):
        bullet.pop("columns", None)


def _bullet_add_excludes(bullet: dict, names: list[str]) -> None:
    if not names:
        return
    element = _bullet_include_element(bullet)
    exclude = element.get("exclude")
    if not isinstance(exclude, list):
        exclude = CommentedSeq()
        element["exclude"] = exclude
    for name in names:
        if name not in exclude:
            exclude.append(name)


def _bullet_exclude_names(bullet: dict) -> list[str]:
    """The bullet's current `exclude` list (read-only; does not create the include element)."""
    for element in bullet.get("columns") or []:
        if isinstance(element, dict) and "include" in element:
            exclude = element.get("exclude")
            return [str(n) for n in exclude] if isinstance(exclude, list) else []
    return []


def _bullet_remove_exclude(bullet: dict, name: str) -> None:
    _bullet_set_exclude(bullet, [n for n in _bullet_exclude_names(bullet) if n.lower() != name.lower()])


def _bullet_config_block(bullet: dict, *, create: bool = False) -> Optional[dict]:
    """The bullet's `config.meta.datacontract_cli` block (created on demand when `create`)."""
    config = bullet.get("config")
    if not isinstance(config, dict):
        if not create:
            return None
        config = CommentedMap()
        bullet["config"] = config
    meta = config.get("meta")
    if not isinstance(meta, dict):
        if not create:
            return None
        meta = CommentedMap()
        config["meta"] = meta
    block = meta.get(META_NAMESPACE)
    if not isinstance(block, dict):
        if not create:
            return None
        block = CommentedMap()
        meta[META_NAMESPACE] = block
    return block


def _bullet_odcs_version(bullet: dict) -> Optional[str]:
    block = _bullet_config_block(bullet)
    version = block.get("contract_version") if block else None
    return version if isinstance(version, str) else None


def _find_bullet_by_odcs(versions: CommentedSeq, odcs_version: str) -> Optional[dict]:
    for bullet in versions:
        if isinstance(bullet, dict) and _bullet_odcs_version(bullet) == odcs_version:
            return bullet
    return None


def _column_has_override(versions: CommentedSeq, col_name: str) -> bool:
    """True if any version bullet already carries a per-version test override for `col_name`."""
    target = col_name.lower()
    for bullet in versions:
        if not isinstance(bullet, dict):
            continue
        for element in bullet.get("columns") or []:
            if (
                isinstance(element, dict)
                and str(element.get("name", "")).lower() == target
                and element.get("data_tests")
            ):
                return True
    return False


def _managed_tests_list(container: dict) -> list:
    return [t for t in (container.get("data_tests") or []) if _meta_block_for_test(t)]


def _set_override_tests(bullet: dict, col_name: str, test_entries: list, contract_version: str) -> None:
    """Put `col_name`'s full test set into `bullet` as a per-version override, stamped with just
    `contract_version`. dbt combines a version override with the (test-free) top-level column, so a
    divergent column must live entirely in overrides — never split across top level and a bullet."""
    _bullet_include_element(bullet)  # an override must ride alongside `include: '*'` (dbt idiom)
    cols = bullet["columns"]
    override = None
    for element in cols:
        if isinstance(element, dict) and str(element.get("name", "")).lower() == col_name.lower():
            override = element
            break
    if override is None:
        override = CommentedMap()
        override["name"] = col_name
        cols.append(override)
    override.pop("data_tests", None)
    tests = CommentedSeq()
    for test in test_entries:
        entry = CommentedMap(test) if not isinstance(test, CommentedMap) else test
        dc_check = _meta_block_for_test(test).get("check")
        _ensure_membership(entry, dc_check, generated=True, contract_version=contract_version, versioned=False)
        tests.append(entry)
    if tests:
        override["data_tests"] = tests


def _managed_test_args(container: dict) -> dict[str, dict]:
    """`{test_name: args}` for the CLI-managed tests on a container (args exclude config/description)."""
    out: dict[str, dict] = {}
    for test in container.get("data_tests") or []:
        if not _meta_block_for_test(test):
            continue
        name = _test_name(test)
        if name is not None:
            out[name] = {k: v for k, v in _test_body(test).items() if k not in ("config", "description")}
    return out


def _desired_test_args(desired_tests: list) -> dict[str, dict]:
    out: dict[str, dict] = {}
    for test in desired_tests or []:
        name = _test_name(test)
        if name is not None:
            out[name] = {k: v for k, v in _test_body(test).items() if k not in ("config", "description")}
    return out


def _bullet_column_override(bullet: dict, col_name: str, *, create: bool = False) -> Optional[dict]:
    """The per-version `{name, ...}` column override element on a bullet (not the `include` element)."""
    target = col_name.lower()
    for element in bullet.get("columns") or []:
        if isinstance(element, dict) and "name" in element and str(element.get("name", "")).lower() == target:
            return element
    if not create:
        return None
    _bullet_include_element(bullet)  # an override must ride alongside `include: '*'` (dbt idiom)
    element = CommentedMap()
    element["name"] = col_name
    bullet["columns"].append(element)
    return element


def _reconcile_versioned_data_types(
    entry: dict,
    versions: CommentedSeq,
    model_dict: dict,
    props_by_name: dict,
    dialect: Optional[str],
    this_version: str,
    run: Optional[Run],
    *,
    prune: bool,
) -> None:
    """Maintain per-version `data_type` on a versioned model.

    Top-level carries the *latest* version's type; every version whose type differs gets a
    minimal `{name, data_type, description}` override.
    """
    latest = str(entry.get("latest_version"))
    unresolved: list[str] = []
    for desired_col in model_dict.get("columns") or []:
        col_name = desired_col["name"]
        prop = props_by_name.get(col_name.lower())
        this_type, status = _resolve_column_type(prop, dialect)
        this_bullet = _find_bullet(versions, this_version)
        top_col = _find_column(entry, col_name)

        if status == "unresolvable":
            unresolved.append(col_name)
            continue
        if status == "silent":
            if prune:
                override = _bullet_column_override(this_bullet, col_name)
                if override is not None:
                    override.pop("data_type", None)
                    _collapse_bare_override(this_bullet, override)
                if str(this_version) == latest and top_col is not None:
                    top_col.pop("data_type", None)
            continue

        # Resolved. Build the {dbt version -> type} map from the current YAML + this sync.
        top_type = top_col.get("data_type") if top_col else None
        type_by_version: dict[str, Optional[str]] = {}
        for b in versions:
            if not isinstance(b, dict):
                continue
            override = _bullet_column_override(b, col_name)
            ov_type = override.get("data_type") if override else None
            type_by_version[str(b.get("v"))] = ov_type if ov_type is not None else top_type
        type_by_version[str(this_version)] = this_type

        if all(t is not None for t in type_by_version.values()):
            new_top = type_by_version.get(latest, this_type)
            if top_col is not None and new_top is not None:
                top_col["data_type"] = new_top
            for b in versions:
                if not isinstance(b, dict):
                    continue
                t = type_by_version.get(str(b.get("v")))
                if t is None:
                    continue
                if t == new_top:
                    override = _bullet_column_override(b, col_name)
                    if override is not None and not override.get("data_tests"):
                        override.pop("data_type", None)
                        _collapse_bare_override(b, override)
                    elif override is not None:
                        # Override survives for divergent tests → keep data_type; dbt doesn't
                        # inherit it into an existing override entry (breaks `contract.enforced`).
                        override["data_type"] = t
                else:
                    override = _bullet_column_override(b, col_name, create=True)
                    override["data_type"] = t
                    _set_override_description(override, top_col)
        else:
            # Some sibling's type is still unknown (e.g. synced before types existed). Don't impose a
            # top-level type on it — put this version's type in its own bullet only.
            override = _bullet_column_override(this_bullet, col_name, create=True)
            override["data_type"] = this_type
            _set_override_description(override, top_col)

    _warn_unresolved_types(run, model_dict.get("name"), unresolved)


def _set_override_description(override: dict, top_col: Optional[dict]) -> None:
    """Stamp the top-level column's description onto an override (dbt doesn't inherit it)."""
    desc = top_col.get("description") if isinstance(top_col, dict) else None
    if desc:
        override["description"] = desc


def _collapse_bare_override(bullet: dict, override: dict) -> None:
    """Drop an override element that's decayed to just `{name}`, then collapse a now-bare `columns`."""
    if set(override.keys()) == {"name"}:
        cols = bullet.get("columns")
        if isinstance(cols, list) and override in cols:
            cols.remove(override)
        _bullet_set_exclude(bullet, _bullet_exclude_names(bullet))  # collapse `columns` if bare


def _merge_versioned_model_entry(
    entry: dict,
    schema_obj: SchemaObject,
    model_dict: dict,
    odcs: OpenDataContractStandard,
    model_version: str,
    *,
    prune: bool,
    defined_in: Optional[str] = None,
    dialect: Optional[str] = None,
    run: Optional[Run] = None,
) -> None:
    """Merge one contract version into a dbt versioned model, additively toward sibling versions.

    Columns/tests common to this version go to the shared top-level `columns:`; a column whose tests
    diverge from what a prior version already put there is emitted as a per-version override. New
    columns are excluded from sibling bullets so their effective set is unchanged.
    """
    props_by_name = {p.name.lower(): p for p in (schema_obj.properties or []) if p.name}
    contract_version = odcs.version or "no_version"

    desc = model_dict.get("description")
    if desc:
        entry["description"] = desc
    elif prune:
        entry.pop("description", None)
    _set_meta_keys(entry, data_contract=odcs.id, owner=(odcs.team.name if odcs.team else None))
    _union_tags(entry, list(schema_obj.tags or []), reconcile=False, prune=prune)

    versions = _ensure_versions_block(entry)
    bullet = _ensure_version_bullet(versions, model_version, defined_in)
    _bullet_config_block(bullet, create=True)["contract_version"] = contract_version  # dbt version → ODCS version

    # Model-level generic tests (composite PK): shared at top level, versions unioned.
    _reconcile_data_tests(entry, model_dict.get("data_tests") or [], contract_version, versioned=True)

    this_cols_lower: set[str] = set()
    newly_created: list[str] = []
    for desired_col in model_dict.get("columns") or []:
        col_name = desired_col["name"]
        this_cols_lower.add(col_name.lower())
        prop = props_by_name.get(col_name.lower())
        target = _find_column(entry, col_name)
        created = target is None
        if created:
            target = CommentedMap()
            target["name"] = col_name
            top_cols = entry.get("columns")
            if not isinstance(top_cols, list):
                top_cols = CommentedSeq()
                entry["columns"] = top_cols
            top_cols.append(target)
            newly_created.append(col_name)
            _mark_managed_column(target)
        if prop is not None and prop.description:
            target["description"] = prop.description.strip().replace("\n", " ")
        elif prune:
            target.pop("description", None)
        col_tags = list(prop.tags or []) if prop else []
        _union_tags(target, col_tags, reconcile=(created or _is_managed_column(target)), prune=prune)

        desired_tests = desired_col.get("data_tests") or []
        top_managed = [] if created else _managed_tests_list(target)
        if created:
            # First writer for this column → share it at top level (dbt forks it per version).
            _reconcile_data_tests(target, desired_tests, contract_version, versioned=True)
        elif _column_has_override(versions, col_name) or not top_managed:
            # Already divergent, or top level has no shared tests to combine with → our bullet only.
            if desired_tests:
                _set_override_tests(bullet, col_name, desired_tests, contract_version)
        elif _managed_test_args(target) == _desired_test_args(desired_tests):
            # Agrees with the shared definition → union this version into it, stay at top level.
            _reconcile_data_tests(target, desired_tests, contract_version, versioned=True)
        else:
            # Divergence appears now: relocate the shared tests into each sharing version's own
            # bullet (dbt would otherwise run BOTH the top-level and the override → duplicate node),
            # then add ours. The top-level column keeps only its name/description.
            shared_versions: set[str] = set()
            for test in top_managed:
                shared_versions.update(_meta_block_for_test(test).get("contract_versions") or [])
            for other_version in shared_versions:
                sibling = _find_bullet_by_odcs(versions, other_version)
                if sibling is not None:
                    _set_override_tests(sibling, col_name, [copy.deepcopy(t) for t in top_managed], other_version)
            target.pop("data_tests", None)
            if desired_tests:
                _set_override_tests(bullet, col_name, desired_tests, contract_version)

    # Excludes: this version drops the top-level columns it doesn't declare; new columns are dropped
    # from every sibling bullet so their effective set is unchanged.
    top_names = [c["name"] for c in entry.get("columns") or [] if isinstance(c, dict) and c.get("name")]
    _bullet_set_exclude(bullet, [n for n in top_names if n.lower() not in this_cols_lower])
    for other in versions:
        if other is not bullet and isinstance(other, dict):
            _bullet_add_excludes(other, newly_created)

    _set_latest_version(entry, versions)
    _reconcile_versioned_data_types(
        entry, versions, model_dict, props_by_name, dialect, model_version, run, prune=prune
    )

    if prune:
        _prune_versioned_entry(entry, model_dict, contract_version, apply=True)


def _prune_versioned_entry(entry: dict, model_dict: dict, contract_version: str, *, apply: bool) -> list[str]:
    """Version-aware prune: retire footprint the synced version alone owned and the contract no
    longer declares. Columns/tests any sibling version still references are left untouched.

    Returns human-readable descriptions of what was (or, with `apply=False`, would be) removed —
    the `apply=False` dry run drives the `--prune` advisory. Mutates `entry` only when `apply`.
    """
    versions = entry.get("versions")
    if not isinstance(versions, list):
        return []
    bullets = [b for b in versions if isinstance(b, dict)]
    this_bullet = _find_bullet_by_odcs(versions, contract_version)
    this_cols_lower = {str(c.get("name", "")).lower() for c in model_dict.get("columns") or []}
    desired_tests_by_col = {
        str(c.get("name", "")).lower(): {_test_name(t) for t in (c.get("data_tests") or []) if _test_name(t)}
        for c in model_dict.get("columns") or []
    }
    removed: list[str] = []

    # 1. Orphaned top-level columns: excluded by every version bullet → no version references them.
    top_cols = entry.get("columns")
    if isinstance(top_cols, list) and bullets:
        orphaned = set.intersection(*({n.lower() for n in _bullet_exclude_names(b)} for b in bullets))
        for col in list(top_cols):
            if isinstance(col, dict) and str(col.get("name", "")).lower() in orphaned:
                removed.append(f"column `{col.get('name')}`")
                if apply:
                    top_cols.remove(col)
                    for b in bullets:
                        _bullet_remove_exclude(b, str(col.get("name", "")))
        if apply and not top_cols:
            entry.pop("columns", None)

    # 2. Retract this version from top-level managed tests it no longer declares (drop the test
    #    once no version is left on it).
    for col in entry.get("columns") or []:
        if not isinstance(col, dict) or str(col.get("name", "")).lower() not in this_cols_lower:
            continue
        desired = desired_tests_by_col.get(str(col.get("name", "")).lower(), set())
        for test in _managed_tests_list(col):
            cvs = _meta_block_for_test(test).get("contract_versions")
            if not isinstance(cvs, list) or contract_version not in cvs or _test_name(test) in desired:
                continue
            removed.append(f"test `{_test_name(test)}` on column `{col.get('name')}`")
            if apply:
                cvs.remove(contract_version)
                if not cvs:
                    col["data_tests"].remove(test)
                    if not col["data_tests"]:
                        col.pop("data_tests", None)

    # 3. Stale per-version overrides on this version's own bullet: CLI-managed override columns it
    #    no longer declares.
    if this_bullet is not None:
        for element in list(this_bullet.get("columns") or []):
            if not isinstance(element, dict) or "name" not in element:
                continue
            if str(element.get("name", "")).lower() in this_cols_lower:
                continue
            tests = element.get("data_tests") or []
            if tests and all(_meta_block_for_test(t) for t in tests):
                removed.append(f"override `{element.get('name')}` on version {this_bullet.get('v')}")
                if apply:
                    this_bullet["columns"].remove(element)
                    _bullet_set_exclude(this_bullet, _bullet_exclude_names(this_bullet))  # collapse if bare

    return removed


