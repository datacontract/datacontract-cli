def _make_yaml(*, sequence: int = 4, offset: int = 2) -> YAML:
    yaml = YAML(typ="rt")
    yaml.preserve_quotes = True
    yaml.width = 4096  # don't wrap long descriptions / regex patterns
    yaml.indent(mapping=2, sequence=sequence, offset=offset)
    return yaml


def _guess_indent(text: str) -> tuple[int, int]:
    """Best-effort (sequence_indent, block_seq_offset) matching the file's own list style."""
    try:
        _, _, offset = load_yaml_guess_indent(text)
    except Exception:
        offset = 2
    return offset + 2, offset


