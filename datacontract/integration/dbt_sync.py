@dataclass
class DbtTestGenerationResult:
    contract_path: Path
    project_dir: Path
    odcs: OpenDataContractStandard
    resolved_models: List[str]
    written_yaml: List[Path]  # model YAML files edited or created
    written_sql: List[Path]
    deleted_files: List[Path]
    skipped_schemas: List[str]
    generation_run: Run


def _resolve_contract_path(contract: Optional[str], search_dir: Path) -> Path:
    if contract:
        contract_path = Path(contract).resolve()
        if not contract_path.is_file():
            raise DataContractException(
                type="dbt_sync",
                name="resolve contract",
                reason=f"Contract file not found: {contract_path}",
                engine="dbt-sync",
            )
        return contract_path
    return find_contract(search_dir)


def generate_dbt_tests(
    *,
    contract: Optional[str],
    project_dir: Optional[Path],
    schema_name: str = "all",
    model_resolution: ModelResolution = ModelResolution.name,
    prune: bool = False,
) -> DbtTestGenerationResult:
    """Resolve the contract, merge its tests + metadata into the dbt project's model YAML in place."""
    project_dir = (project_dir or Path.cwd()).resolve()
    _ensure_dbt_project(project_dir)

    contract_path = _resolve_contract_path(contract, project_dir)
    odcs = resolve_data_contract(str(contract_path))
    logger.info(f"Resolved contract {odcs.id}@{odcs.version} from {contract_path}")

    run = Run.create_run()
    run.dataContractId = odcs.id
    run.dataContractVersion = odcs.version

    name_map = resolve_model_names(odcs, model_resolution, schema_name)
    if not name_map:
        run.log_info("No quality to sync (contract has no schemas with names).")

    model_paths, _ = _read_dbt_project_paths(project_dir)
    legacy_dir, tests_dir = _resolved_generated_dirs(project_dir)

    # Load every model-path YAML up front so model names can be matched case-insensitively
    # against the project's actual dbt models (#1254) before we generate anything.
    session = _EditSession()
    for path in _iter_model_yaml_files(model_paths, legacy_dir):
        session.try_load(path)

    # Resolve each schema to the project's actual dbt model name (case-preserved), then build
    # the desired state (model dict + singular SQL) against that name so `ref()` / patches match.
    schemas_by_name = {s.name: s for s in (odcs.schema_ or []) if s.name}
    plans: List[_ModelPlan] = []
    skipped_schemas: List[str] = []
    for schema_name_key, contract_model_name in name_map.items():
        effective, source, sql_path = _effective_model_name(session, model_paths, contract_model_name)
        if source == "missing":
            run.log_warn(
                f"Schema `{schema_name_key}` resolves to model `{contract_model_name}`, which has no matching "
                "dbt model (no `.sql` or YAML entry) in this project — nothing to test, skipping. "
                "If the model exists under a different name, try `--model-resolution physicalName`."
            )
            skipped_schemas.append(schema_name_key)
            continue
        schema_obj = schemas_by_name[schema_name_key]
        model_dict, singulars = generate_dbt_tests_for_schema(odcs, schema_obj, effective, run)
        plans.append(_ModelPlan(effective, schema_obj, model_dict, singulars, sql_path))

    singular_tests: List[SingularTest] = [s for p in plans for s in p.singular_tests]

    required = RequiredState(
        scope_models={p.name.lower(): p.name for p in plans},
        required_tests={p.name.lower(): _required_test_keys(p.model_dict) for p in plans},
    )
    _run_yaml_cleanup(session, required, scope_contract_id=odcs.id)

    resolved_models: List[str] = []
    for plan in plans:
        located = _locate_entry(session, plan.name)
        if located is not None:
            path, entry = located
            owned = _is_owned_sidecar(session.files[path].original_text)
            _merge_model_entry(entry, plan.schema_obj, plan.model_dict, odcs, owned=owned, prune=prune)
        else:
            # source == "sql": no YAML entry anywhere → sidecar next to the model's `.sql`.
            sidecar_path = plan.sql_path.with_suffix(".yml")
            existing = session.files.get(sidecar_path)
            entry = CommentedMap()
            entry["name"] = plan.name
            if existing is not None:
                owned = _is_owned_sidecar(existing.original_text)
                models = existing.data.get("models")
                if not isinstance(models, list):
                    models = CommentedSeq()
                    existing.data["models"] = models
                models.append(entry)
                _merge_model_entry(entry, plan.schema_obj, plan.model_dict, odcs, owned=owned, prune=prune)
            else:
                session.create_sidecar(sidecar_path, entry)
                _merge_model_entry(entry, plan.schema_obj, plan.model_dict, odcs, owned=True, prune=prune)
        resolved_models.append(plan.name)

    # Singular SQL is CLI-owned wholesale: wipe + regenerate. Migrate the legacy parallel-YAML dir.
    _, tests_dir = _resolved_generated_dirs(project_dir)
    if tests_dir.exists():
        shutil.rmtree(tests_dir)
    tests_dir.mkdir(parents=True, exist_ok=True)

    written_sql = _write_singular_sql(project_dir, singular_tests)
    _remove_legacy_dir(legacy_dir)
    written_yaml, deleted_files = session.apply()

    return DbtTestGenerationResult(
        contract_path=contract_path,
        project_dir=project_dir,
        odcs=odcs,
        resolved_models=resolved_models,
        written_yaml=written_yaml,
        written_sql=written_sql,
        deleted_files=deleted_files,
        skipped_schemas=skipped_schemas,
        generation_run=run,
    )


def run_tests(
    odcs: OpenDataContractStandard,
    project_dir: Path,
    *,
    target: Optional[str] = None,
    profiles_dir: Optional[Path] = None,
    generation_run: Optional[Run] = None,
) -> Run:
    """Run `dbt test` against the contract-managed tests on disk and return the parsed results.

    `generation_run` is the `Run` from a same-invocation `generate_dbt_tests` (sync's `--run-tests`
    path); its logs and start time are stitched onto the result. `dbt test` (run-only) passes None.
    """
    completed = run_dbt_test(project_dir, target=target, profiles_dir=profiles_dir)

    parsed_run = parse_run_results(project_dir, odcs)
    ansi = re.compile(r"\x1b\[[0-9;]*[mGKHF]")
    for stream in (completed.stdout, completed.stderr):
        if not stream:
            continue
        for line in ansi.sub("", stream).splitlines():
            line = line.strip()
            if line:
                parsed_run.log_info(line)

    if generation_run is not None:
        # Stitch generation-time logs on so warn-counts include any generation-time skips.
        parsed_run.logs = generation_run.logs + parsed_run.logs
        parsed_run.timestampStart = generation_run.timestampStart
    parsed_run.finish()
    return parsed_run
