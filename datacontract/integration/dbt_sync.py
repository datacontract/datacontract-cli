def _is_managed_column(col: dict) -> bool:
    meta = col.get("meta")
    block = meta.get(META_NAMESPACE) if isinstance(meta, dict) else None
    return isinstance(block, dict) and block.get("generated") is True


def _mark_managed_column(col: dict) -> None:
    meta = col.get("meta")
    if not isinstance(meta, dict):
        meta = CommentedMap()
        col["meta"] = meta
    block = meta.get(META_NAMESPACE)
    if not isinstance(block, dict):
        block = CommentedMap()
        meta[META_NAMESPACE] = block
    block["generated"] = True


def _find_column(entry: dict, name: str) -> Optional[dict]:
    """Case-insensitive lookup of a column by name (preserves the file's own casing)."""
    target = name.lower()
    for col in entry.get("columns") or []:
        if isinstance(col, dict) and str(col.get("name", "")).lower() == target:
            return col
    return None


def _existing_config(container: dict) -> Optional[dict]:
    config = container.get("config")
    return config if isinstance(config, dict) else None


def _ensure_config(container: dict) -> dict:
    config = container.get("config")
    if not isinstance(config, dict):
        config = CommentedMap()
        container["config"] = config
    return config


def _union_tags(container: dict, new_tags: list, *, reconcile: bool, prune: bool) -> None:
    """Maintain `config.tags` on a model/column entry from ODCS tags.

    `reconcile` (entry wholly ours) / `prune` → mirror the contract (add + drop orphans);
    otherwise union, never dropping the user's own tags. Never creates an empty `config`.
    """
    mirror = reconcile or prune
    config = _existing_config(container)
    existing = config.get("tags") if config else None
    existing = list(existing) if isinstance(existing, list) else None

    if existing is None:
        if not new_tags:
            return  # nothing to add and nothing to mirror away
        result = list(new_tags)
    elif mirror:
        result = [t for t in existing if t in new_tags]
        for t in new_tags:
            if t not in result:
                result.append(t)
    else:
        result = existing
        for t in new_tags:
            if t not in result:
                result.append(t)

    config = _ensure_config(container)
    if result:
        config["tags"] = result
    else:
        config.pop("tags", None)
        if not config:
            container.pop("config", None)


def _set_meta_keys(entry: dict, *, data_contract: Optional[str], owner: Optional[str]) -> None:
    """Make our `config.meta` keys authoritative: write `data_contract`/`owner` from the contract,
    overwriting prior values, and drop a stale `owner` when the contract has no team. Other meta is kept."""
    config = _ensure_config(entry)
    meta = config.get("meta")
    if not isinstance(meta, dict):
        meta = CommentedMap()
        config["meta"] = meta
    if data_contract is not None:
        meta["data_contract"] = data_contract
    if owner is not None:
        meta["owner"] = owner
    else:
        meta.pop("owner", None)


def _meta_data_contract(entry: dict) -> Optional[str]:
    meta = (entry.get("config") or {}).get("meta") or {}
    val = meta.get("data_contract")
    return val if isinstance(val, str) else None


def _remove_our_meta(entry: dict) -> None:
    """Drop the named `config.meta` keys we set, collapsing emptied containers."""
    config = entry.get("config")
    if not isinstance(config, dict):
        return
    meta = config.get("meta")
    if isinstance(meta, dict):
        meta.pop("data_contract", None)
        meta.pop("owner", None)
        if not meta:
            config.pop("meta", None)
    if not config:
        entry.pop("config", None)


