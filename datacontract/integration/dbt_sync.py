def _locate_versioned_model_sql(model_paths: list[Path], model_name: str, model_version: str) -> Optional[Path]:
    """First `<model>_v<N>.sql` whose numeric version equals `model_version` (so `orders_v01.sql`
    matches version "1"). This is how the dbt files for a versioned model are found on disk."""
    base, want = model_name.lower(), int(model_version)
    matches: list[Path] = []
    for root in model_paths:
        if not root.is_dir():
            continue
        for path in root.rglob("*.sql"):
            m = _VERSIONED_SQL_RE.match(path.stem.lower())
            if m and m.group("base") == base and int(m.group("v")) == want:
                matches.append(path)
    return sorted(matches)[0] if matches else None


@dataclass
class RequiredState:
    """What the current contract declares — drives the provenance-aware cleanup pass."""

    scope_models: dict[str, str] = field(default_factory=dict)  # lower(model) -> model name
    required_tests: dict[str, set[tuple[Optional[str], str]]] = field(default_factory=dict)


def _required_test_keys(model_dict: dict) -> set[tuple[Optional[str], str]]:
    keys: set[tuple[Optional[str], str]] = set()
    for test in model_dict.get("data_tests") or []:
        name = _test_name(test)
        if name:
            keys.add((None, name))
    for col in model_dict.get("columns") or []:
        col_lower = str(col.get("name", "")).lower()
        for test in col.get("data_tests") or []:
            name = _test_name(test)
            if name:
                keys.add((col_lower, name))
    return keys


@dataclass
class _ModelPlan:
    """One contract schema resolved to the project's actual dbt model, with its desired state."""

    name: str  # effective dbt model name (case as the project has it)
    schema_obj: SchemaObject
    model_dict: dict
    singular_tests: list["SingularTest"]
    sql_path: Optional[Path]  # set only when matched via a `.sql` file (→ sidecar)


def _effective_model_name(
    session: "_EditSession", model_paths: list[Path], contract_model_name: str, model_version: Optional[str] = None
) -> tuple[str, str, Optional[Path]]:
    """Map a contract model name to the project's actual dbt model name (case-insensitive, #1254).

    Returns ``(name, source, sql_path)`` where ``source`` is ``"entry"`` (a YAML model entry
    exists), ``"sql"`` (only a `.sql` exists → sidecar), or ``"missing"`` (no dbt model).
    The returned name preserves the project's casing so `ref()` and dbt patches resolve. For a
    versioned sync the model's files are `<model>_v<N>.sql`, so `sql_path` points at this version's
    file while the name stays the base model.
    """
    located = _locate_entry(session, contract_model_name)
    if located is not None:
        entry = located[1]
        return str(entry.get("name") or contract_model_name), "entry", None
    if model_version is not None:
        versioned_sql = _locate_versioned_model_sql(model_paths, contract_model_name, model_version)
        if versioned_sql is not None:
            return contract_model_name, "sql", versioned_sql
        return contract_model_name, "missing", None
    sql_path = _locate_model_sql(model_paths, contract_model_name)
    if sql_path is not None:
        return sql_path.stem, "sql", sql_path
    return contract_model_name, "missing", None


# ---------------------------------------------------------------------------
# Edit session — accumulates YAML edits across the cleanup + merge passes, then
# writes them in one shot.
# ---------------------------------------------------------------------------


@dataclass
class _PlannedFile:
    path: Path
    data: Any
    yaml: YAML
    original_text: Optional[str]  # None => created this run
    deleted: bool = False


class _EditSession:
    def __init__(self) -> None:
        self.files: dict[Path, _PlannedFile] = {}
        self.unparseable: list[Path] = []  # existing YAML that failed to parse

    def try_load(self, path: Path) -> Optional[Any]:
        if path in self.files:
            return self.files[path].data
        try:
            text = path.read_text(encoding="utf-8")
            seq_indent, offset = _guess_indent(text)
            y = _make_yaml(sequence=seq_indent, offset=offset)
            data = y.load(text)
        except Exception:
            self.unparseable.append(path)
            return None  # unparseable / unreadable → leave the file untouched
        if not isinstance(data, dict):
            return None
        self.files[path] = _PlannedFile(path=path, data=data, yaml=y, original_text=text)
        return data

    def create_sidecar(self, path: Path, entry: dict) -> Any:
        """Seed a new model YAML file. It's a plain dbt schema file — free to edit; sync merges
        into it additively on later runs, the same as a file the user wrote themselves."""
        data = CommentedMap()
        data["version"] = 2
        models = CommentedSeq()
        models.append(entry)
        data["models"] = models
        self.files[path] = _PlannedFile(path=path, data=data, yaml=_make_yaml(), original_text=None)
        return data

    def delete(self, path: Path) -> None:
        pf = self.files.get(path)
        if pf is not None:
            pf.deleted = True

    def _render(self, pf: _PlannedFile) -> str:
        buf = StringIO()
        pf.yaml.dump(pf.data, buf)
        return buf.getvalue()

    def apply(self) -> tuple[list[Path], list[Path]]:
        written: list[Path] = []
        deleted: list[Path] = []
        for pf in self.files.values():
            if pf.deleted:
                if pf.path.exists():
                    pf.path.unlink()
                    deleted.append(pf.path)
                continue
            new_text = self._render(pf)
            if new_text != (pf.original_text or ""):
                pf.path.parent.mkdir(parents=True, exist_ok=True)
                pf.path.write_text(new_text, encoding="utf-8")
                written.append(pf.path)
        return written, deleted


def _locate_entry(session: _EditSession, model_name: str) -> Optional[tuple[Path, dict]]:
    """First `models[].name` match across loaded files (case-insensitive, deterministic order)."""
    target = model_name.lower()
    for path in sorted(session.files):
        if session.files[path].deleted:
            continue
        for entry in _model_entries(session.files[path].data):
            if str(entry.get("name", "")).lower() == target:
                return path, entry
    return None


