def run_dbt_test(
    project_dir: Path,
    *,
    target: Optional[str],
    profiles_dir: Optional[Path],
    model_versions: Optional[list[tuple[str, Optional[str], str]]] = None,
) -> subprocess.CompletedProcess:
    # Selects every CLI-managed test (generated + adopted) by the meta key all of them carry.
    # When `model_versions` is given, scope to managed tests on those `(model, dbt version, contract
    # version)` triples only so a multi-contract run executes only the requested contracts' tests,
    # and tests from a previously-synced version aren't picked up.
    select_managed = f"config.meta.{META_NAMESPACE}.include_in_tests:true"
    args = ["dbt", "test", "--project-dir", str(project_dir)]
    if model_versions:
        for model, dbt_version, version in model_versions:
            version_scope = f"config.meta.{META_NAMESPACE}.contract_versions:{version}"
            # Two selectors per model: the first catches generic tests (linked to the model via a
            # graph edge — scoped to this dbt version's node when the model is versioned), the second
            # catches singular SQL tests (no graph edge — they carry the owning model in their
            # `meta.model` instead). Both scoped to the contract version.
            node = f"{model}.v{dbt_version}" if dbt_version else model
            args.extend(["--select", f"{node},{select_managed},{version_scope}"])
            args.extend(["--select", f"config.meta.{META_NAMESPACE}.model:{model},{select_managed},{version_scope}"])
    else:
        args.extend(["--select", select_managed])
    if target:
        args.extend(["--target", target])
    if profiles_dir:
        # Resolve to absolute — we cwd into project_dir below, so a relative
        # `--profiles-dir` from the user's shell would otherwise miss.
        args.extend(["--profiles-dir", str(Path(profiles_dir).resolve())])

    # Drop any prior run_results.json so we don't mistake a stale file for fresh output
    # if dbt fails before regenerating it.
    run_results_path = _resolved_target_dir(project_dir) / "run_results.json"
    if run_results_path.is_file():
        run_results_path.unlink()

    try:
        # cwd=project_dir lets dbt find a project-local profiles.yml the way users expect
        # when running `dbt test` from inside their project.
        result = subprocess.run(args, capture_output=True, text=True, check=False, cwd=str(project_dir))
    except OSError as e:
        raise DataContractException(
            type="dbt_sync",
            name="dbt test",
            reason=f"Failed to invoke dbt: {e}",
            engine="dbt-sync",
            original_exception=e,
        )

    ansi_control_chars = re.compile(r"\x1b\[[0-9;]*[mGKHF]")
    output = ansi_control_chars.sub("", (result.stderr or "") + (result.stdout or ""))
    if result.returncode != 0 and not run_results_path.is_file():
        raise DataContractException(
            type="dbt_sync",
            name="dbt test",
            reason=f"`dbt test` failed (exit code {result.returncode}):\n{output}",
            engine="dbt-sync",
        )
    return result


def _load_manifest(project_dir: Path) -> dict:
    manifest_path = _resolved_target_dir(project_dir) / "manifest.json"
    if not manifest_path.is_file():
        return {}
    try:
        with manifest_path.open("r", encoding="utf-8") as f:
            return json.load(f)
    except (OSError, json.JSONDecodeError):
        return {}


def _get_test_metadata(manifest_node: dict, manifest_nodes: dict) -> tuple[Optional[str], Optional[str], Optional[str]]:
    """Return (model, column, description) extracted from a manifest.json test node."""
    if not manifest_node:
        return None, None, None
    description = manifest_node.get("description") or None

    column_name = manifest_node.get("column_name")
    if not column_name:
        test_metadata = manifest_node.get("test_metadata") or {}
        kwargs = test_metadata.get("kwargs") or {}
        column_name = kwargs.get("column_name")

    model = None
    attached = manifest_node.get("attached_node")
    model_ids = ([attached] if attached else []) + ((manifest_node.get("depends_on") or {}).get("nodes") or [])
    for model_id in model_ids:
        if isinstance(model_id, str) and model_id.startswith("model."):
            model = (manifest_nodes.get(model_id) or {}).get("name")
            break

    # Singular SQL tests have no `column_name` / `attached_node` in the manifest,
    # therefore we stored them to `config(meta={datacontract_cli: {...}})`.
    meta = (manifest_node.get("config") or {}).get("meta") or {}
    dc = meta.get(META_NAMESPACE) or {}
    if not column_name and dc.get("field"):
        column_name = dc["field"]
    if not model and dc.get("model"):
        model = dc["model"]
    if not description and dc.get("description"):
        description = dc["description"]

    return model, column_name, description


def parse_run_results_file(
    project_dir: Path, odcs: OpenDataContractStandard, *, model_filter: Optional[set[str]] = None
) -> Run:
    """Parse the run_results.json file from a `dbt test` run."""
    run = Run.create_run()
    run.dataContractId = odcs.id
    run.dataContractVersion = odcs.version

    run_results_path = _resolved_target_dir(project_dir) / "run_results.json"
    if not run_results_path.is_file():
        run.log_warn(f"`{run_results_path}` not found — no test results to record.")
        run.finish()
        return run

    try:
        with run_results_path.open("r", encoding="utf-8") as f:
            run_results = json.load(f)
    except (OSError, json.JSONDecodeError) as e:
        run.log_warn(f"Could not read `{run_results_path}` — {e}")
        run.finish()
        return run

    # dbt creates a manifest node for each model, test, source etc.
    dbt_manifest = _load_manifest(project_dir)
    manifest_nodes = dbt_manifest.get("nodes") or {}

    status_map = {
        "pass": ResultEnum.passed,
        "fail": ResultEnum.failed,
        "warn": ResultEnum.warning,
        "error": ResultEnum.error,
        "skipped": ResultEnum.info,
    }
    for test_result in run_results.get("results") or []:
        unique_id = test_result.get("unique_id") or ""
        status = (test_result.get("status") or "").lower()
        result_enum = status_map.get(status, ResultEnum.unknown)
        failures = test_result.get("failures")
        message = test_result.get("message")
        manifest_node = manifest_nodes.get(unique_id) or {}
        model, column, description = _get_test_metadata(manifest_node, manifest_nodes)
        if model_filter is not None and (model or "").lower() not in model_filter:
            continue  # belongs to another contract's models — attributed elsewhere
        dc_meta = (manifest_node.get("config") or {}).get("meta") or {}
        dc_block = dc_meta.get(META_NAMESPACE) if isinstance(dc_meta.get(META_NAMESPACE), dict) else {}
        # `check` holds the fully-qualified key (`{model}__{field}__{type}`). The bare type is its
        # final `__`-segment — unambiguous because no check-type token itself contains `__`.
        check_key = dc_block.get("check") if isinstance(dc_block.get("check"), str) else None
        check_type = check_key.rsplit("__", 1)[-1] if check_key else "dbt_test"
        fallback_name = manifest_node.get("name")
        if not fallback_name:
            parts = unique_id.split(".")
            fallback_name = parts[-2] if len(parts) >= 3 else unique_id
        reason_parts = []
        if failures:
            reason_parts.append(f"failures={failures}")
        if message:
            reason_parts.append(message)
        run.checks.append(
            Check(
                key=check_key,
                type=check_type,
                name=description or fallback_name,
                model=model,
                field=column,
                engine="dbt",
                result=result_enum,
                reason=" | ".join(reason_parts) or None,
            )
        )

    run.finish()
    return run


