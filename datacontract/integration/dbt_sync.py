def _resolve_type_dialect(odcs: OpenDataContractStandard, server: Optional[str]) -> Optional[str]:
    """The SQL dialect used to map a `logicalType` to a `data_type`, from the ODCS server `type`."""
    servers = odcs.servers or []
    if server:
        for s in servers:
            if s.server == server and s.type:
                return s.type
    types = {s.type for s in servers if s.type}
    return next(iter(types)) if len(types) == 1 else None


def _resolve_column_type(prop: SchemaProperty, dialect: Optional[str]) -> tuple[Optional[str], str]:
    """(`data_type`, status) for a column, status ∈ {`resolved`, `silent`, `unresolvable`}.

    `physicalType` → verbatim (dialect-independent). `logicalType` → mapped through `dialect`;
    `unresolvable` when the contract declares a logical type but no dialect resolves it (skip +
    warn, never remove). `silent` when the contract declares no type at all (leave; `--prune` drops).
    """
    physical = prop.physicalType or _get_config_value(prop, "physicalType")
    logical = prop.logicalType
    if not physical and not logical:
        return None, "silent"
    if physical:
        if not dialect:
            return str(physical), "resolved"  # no warehouse to normalize to → author's type verbatim
        try:
            return convert_to_sql_type(prop, dialect), "resolved"
        except Exception:
            return str(physical), "resolved"
    if not dialect:
        return None, "unresolvable"
    try:
        dt = convert_to_sql_type(prop, dialect)
    except Exception:
        dt = None
    return (dt, "resolved") if dt else (None, "unresolvable")


def _warn_unresolved_types(run: Optional[Run], model: Optional[str], columns: list[str]) -> None:
    """Warn once that logical-only columns couldn't be typed for lack of a resolvable dialect."""
    if run is None or not columns:
        return
    run.log_warn(
        f"`{model}`: could not determine the SQL dialect for the `data_type` of column(s) "
        f"{', '.join(columns)} — pass `--server <name>` or set `physicalType` in the contract."
    )


def _apply_column_data_type(target: dict, prop: SchemaProperty, dialect: Optional[str], *, prune: bool) -> str:
    """Write `data_type` onto a column entry per the contract-authoritative rule. Returns the status."""
    dt, status = _resolve_column_type(prop, dialect)
    if status == "resolved" and dt is not None:
        target["data_type"] = dt
    elif status == "silent" and prune:
        target.pop("data_type", None)
    return status


def _column_dict(
    prop: SchemaProperty, odcs: OpenDataContractStandard, single_pk_name: Optional[str], model_name: str, run: Run
) -> dict:
    column: dict = {"name": prop.name}
    base = field_to_data_tests(
        prop,
        is_primary_key=bool(prop.primaryKey),
        is_single_pk=(prop.name == single_pk_name),
        supports_constraints=False,
        source_name=odcs.id or "_source",
        include_dbt_expectations_bounds=False,
    )
    base = _rewrite_relationships_to_ref(base)
    tests = [
        _attach_test_config(
            test,
            severity="warn",
            description=_describe_dbt_test(test, prop.name, model_name),
            check_type=_check_type_for_generic_test(test),
            model=model_name,
            field=prop.name,
        )
        for test in base
    ]

    for data_quality in prop.quality or []:
        if data_quality.metric and data_quality.metric.lower() == "invalidvalues":
            continue
        if data_quality.query:
            continue
        if data_quality.mustBe is not None:
            entry = {"accepted_values": {"values": [data_quality.mustBe]}}
            tests.append(
                _attach_test_config(
                    entry,
                    _normalize_severity(data_quality.severity),
                    description=data_quality.description or _describe_dbt_test(entry, prop.name, model_name),
                    check_type="field_enum",
                    model=model_name,
                    field=prop.name,
                )
            )
            continue
        run.log_warn(
            f"Skipping unsupported quality on `{prop.name}`: metric={data_quality.metric!r} — please raise an issue on GitHub if this quality check should be added: https://github.com/datacontract/datacontract-cli/issues"
        )

    if tests:
        column["data_tests"] = tests
    return column


def generate_dbt_tests_for_schema(
    odcs: OpenDataContractStandard,
    schema_obj: SchemaObject,
    model_name: str,
    run: Run,
    model_version: Optional[str] = None,
) -> tuple[dict, list[SingularTest]]:
    """Build the YAML model dict + list of singular SQL tests for one schema.

    `model_version` (set for a versioned model) makes ref-using singular tests target this version's
    relation via `ref(model, version=N)`.
    """
    pk_cols = [p.name for p in (schema_obj.properties or []) if p.primaryKey]
    single_pk_name = pk_cols[0] if len(pk_cols) == 1 else None

    model_dict: dict = {"name": model_name}
    if schema_obj.description:
        model_dict["description"] = schema_obj.description.strip().replace("\n", " ")

    contract_id = odcs.id or "contract"
    contract_version = odcs.version or "no_version"
    model_singulars = _model_data_tests(schema_obj, run, contract_id, contract_version, model_name, model_version)

    columns: list = []
    for prop in schema_obj.properties or []:
        columns.append(_column_dict(prop, odcs, single_pk_name, model_name, run))
    if columns:
        model_dict["columns"] = columns

    singulars: list[SingularTest] = list(model_singulars)
    # Field-level bounds (length / regex / numeric range) → singular SQL
    for prop in schema_obj.properties or []:
        singulars.extend(_field_singular_tests(prop, contract_id, contract_version, model_name, model_version))
    # Existing `quality.query` singular tests (one CTE per scalar metric)
    for prop in schema_obj.properties or []:
        singulars.extend(
            _singular_tests_for_qualities(
                prop.quality,
                contract_id,
                contract_version,
                model_name,
                run,
                label_prefix=f"{prop.name}__",
                field=prop.name,
            )
        )
    singulars.extend(_singular_tests_for_qualities(schema_obj.quality, contract_id, contract_version, model_name, run))
    _disambiguate_singular_filenames(singulars)

    return model_dict, singulars


def _disambiguate_singular_filenames(singular_tests: list[SingularTest]) -> None:
    """In-place rename so two qualities whose labels slugify identically don't overwrite each other."""
    used: set[str] = set()
    for test in singular_tests:
        if test.filename not in used:
            used.add(test.filename)
            continue
        stem = Path(test.filename).stem
        n = 2
        while f"{stem}__{n}.sql" in used:
            n += 1
        new_filename = f"{stem}__{n}.sql"
        test.filename = new_filename
        used.add(new_filename)


