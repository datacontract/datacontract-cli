def _entry_has_footprint(entry: dict) -> bool:
    if _meta_data_contract(entry):
        return True
    if any(_meta_block_for_test(t) for t in (entry.get("data_tests") or [])):
        return True
    for col in entry.get("columns") or []:
        if isinstance(col, dict) and (
            _is_managed_column(col) or any(_meta_block_for_test(t) for t in (col.get("data_tests") or []))
        ):
            return True
    return False


def _entry_in_scope(entry: dict, scope_contract_id: Optional[str], scope_models: dict[str, str]) -> bool:
    if scope_contract_id is None:
        return _entry_has_footprint(entry)
    return _meta_data_contract(entry) == scope_contract_id or str(entry.get("name", "")).lower() in scope_models


def _clean_tests_in_container(
    container: dict,
    column_key: Optional[str],
    required_keys: set[tuple[Optional[str], str]],
    *,
    model_required: bool,
) -> None:
    tests = container.get("data_tests")
    if not isinstance(tests, list):
        return
    survivors = CommentedSeq()
    for entry in tests:
        if not _meta_block_for_test(entry):
            survivors.append(entry)
            continue
        name = _test_name(entry)
        if model_required and (column_key, name) in required_keys:
            survivors.append(entry)  # still required → merge will refresh it
            continue
        if _meta_block_for_test(entry).get("generated") is True:
            continue  # drop the node we created
        _strip_managed(entry)  # drop our meta block, keep the user's test
        # Collapse `{name: {}}` back to the bare string `name` if stripping emptied it.
        collapsed = name if isinstance(entry, dict) and not entry[name] else entry
        survivors.append(collapsed)
    if len(survivors):
        container["data_tests"] = survivors
    else:
        container.pop("data_tests", None)


def _clean_model_entry(
    entry: dict,
    *,
    required_keys: set[tuple[Optional[str], str]],
    model_required: bool,
) -> None:
    _clean_tests_in_container(entry, None, required_keys, model_required=model_required)
    for col in entry.get("columns") or []:
        if isinstance(col, dict):
            _clean_tests_in_container(
                col,
                str(col.get("name", "")).lower(),
                required_keys,
                model_required=model_required,
            )

    cols = entry.get("columns")
    if isinstance(cols, list):
        survivors = CommentedSeq()
        for col in cols:
            if (
                isinstance(col, dict)
                and _is_managed_column(col)
                and not col.get("data_tests")
                and not col.get("description")
                and not (col.get("data_type") and model_required)
            ):
                continue  # drop a managed column that has become empty
            survivors.append(col)
        if len(survivors):
            entry["columns"] = survivors
        else:
            entry.pop("columns", None)

    if not model_required:
        # The contract no longer declares this model; retire the entry we generated for it. Drop our
        # meta and the contract-sourced description so a purely-generated entry reduces to `{name}`
        # (an entry the user also put their own columns/tests on keeps those and survives).
        entry.pop("description", None)
        _remove_our_meta(entry)


def _run_yaml_cleanup(
    session: _EditSession,
    required: RequiredState,
    *,
    scope_contract_id: Optional[str],
) -> None:
    """Walk every loaded file and remove CLI footprint that is no longer required.

    An orphaned entry we generated (the contract no longer declares its model) is stripped and, once
    nothing but its name remains, removed. A file we emptied this way — no models left and no other
    content (sources, exposures, ...) — is deleted, whether or not the CLI first created it.
    """
    for path in sorted(session.files):
        pf = session.files[path]
        if pf.deleted:
            continue
        models = pf.data.get("models") if isinstance(pf.data, dict) else None
        if not isinstance(models, list):
            continue
        touched = False
        drop_indices: list[int] = []
        for i, entry in enumerate(models):
            if not isinstance(entry, dict) or not _entry_in_scope(entry, scope_contract_id, required.scope_models):
                continue
            touched = True
            model_lower = str(entry.get("name", "")).lower()
            model_required = model_lower in required.scope_models
            _clean_model_entry(
                entry,
                required_keys=required.required_tests.get(model_lower, set()),
                model_required=model_required,
            )
            # An entry stripped down to just its `name` has nothing left to keep.
            if not model_required and set(entry.keys()) <= {"name"}:
                drop_indices.append(i)

        if not touched:
            continue
        for i in reversed(drop_indices):
            del models[i]  # in place, so the CommentedSeq keeps its formatting for the survivors
        # Delete a file we emptied out, provided it holds nothing else (only `version` / `models`).
        if not models and not (set(pf.data.keys()) - {"version", "models"}):
            session.delete(path)


