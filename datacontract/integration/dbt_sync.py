def _test_name(entry: str | dict) -> Optional[str]:
    if isinstance(entry, str):
        return entry
    if isinstance(entry, dict) and len(entry) == 1:
        return next(iter(entry.keys()))
    return None


def _test_body(entry: str | dict) -> dict:
    """The mapping under the test's single key (e.g. `{values:…, config:…}`); `{}` for a bare string."""
    if isinstance(entry, dict) and len(entry) == 1:
        body = next(iter(entry.values()))
        if isinstance(body, dict):
            return body
    return {}


def _meta_block_for_test(entry: str | dict) -> dict:
    """The CLI's `config.meta.datacontract_cli` block on a test entry, or `{}`."""
    meta = (_test_body(entry).get("config") or {}).get("meta") or {}
    block = meta.get(META_NAMESPACE)
    return block if isinstance(block, dict) else {}


def _ensure_membership(
    entry: dict,
    dc_check: Optional[str],
    *,
    generated: bool,
    contract_version: Optional[str] = None,
    versioned: bool = False,
) -> None:
    """Stamp the CLI's `config.meta.datacontract_cli` block onto a test.

    `generated=True` marks a test we created; `generated=False` records an adopted user test but
    never downgrades an already-`True` block. `contract_version` lets `dbt test` scope by version:
    on a versioned model it's unioned into `contract_versions` (a shared column serves several
    versions); otherwise it replaces the list (an in-place model serves one version at a time).
    """
    body = entry[_test_name(entry)]
    config = body.get("config")
    if not isinstance(config, dict):
        config = CommentedMap()
        body["config"] = config
    meta = config.get("meta")
    if not isinstance(meta, dict):
        meta = CommentedMap()
        config["meta"] = meta
    block = meta.get(META_NAMESPACE)
    if not isinstance(block, dict):
        block = CommentedMap()
        meta[META_NAMESPACE] = block

    if "include_in_tests" not in block:
        block["include_in_tests"] = True  # default; a user may flip to false to opt the test out
    if dc_check:
        block["check"] = dc_check
    if contract_version is not None:
        existing = block.get("contract_versions")
        if versioned and isinstance(existing, list):
            if contract_version not in existing:
                existing.append(contract_version)
        else:
            block["contract_versions"] = [contract_version]
    if generated:
        block["generated"] = True
    elif "generated" not in block:
        block["generated"] = False


def _strip_managed(entry: dict) -> bool:
    """Remove the CLI's `config.meta.datacontract_cli` block from a test, leaving the user's test. True if modified."""
    name = _test_name(entry)
    if name is None or isinstance(entry, str):
        return False
    body = entry[name]
    config = body.get("config")
    if not isinstance(config, dict):
        return False
    changed = False
    meta = config.get("meta")
    if isinstance(meta, dict) and META_NAMESPACE in meta:
        meta.pop(META_NAMESPACE, None)
        if not meta:
            config.pop("meta", None)
        changed = True
    if not config:
        body.pop("config", None)
    return changed


def _reconcile_test(
    existing: str | dict,
    desired: dict,
    dc_check: Optional[str],
    contract_version: Optional[str],
    *,
    versioned: bool = False,
) -> dict:
    """Update a pre-existing test (string or dict) to satisfy the contract, in dict form.

    Overwrites args to the contract's (contract wins), then ensures membership tags. The user's
    `config` (severity etc.) and any non-arg keys are left untouched.
    """
    name = _test_name(existing)
    if isinstance(existing, str):
        entry: dict = CommentedMap()
        entry[name] = CommentedMap()
    else:
        entry = existing
        if not isinstance(entry.get(name), dict):
            entry[name] = CommentedMap()
    body = entry[name]
    desired_args = {k: v for k, v in _test_body(desired).items() if k not in ("config", "description")}
    for k in [k for k in list(body.keys()) if k not in ("config", "description") and k not in desired_args]:
        del body[k]
    for k, v in desired_args.items():
        body[k] = v
    _ensure_membership(entry, dc_check, generated=False, contract_version=contract_version, versioned=versioned)
    return entry


def _reconcile_data_tests(
    container: dict, desired_tests: list, contract_version: Optional[str], *, versioned: bool = False
) -> None:
    """Merge the contract's desired generic tests into `container["data_tests"]` in place."""
    if not desired_tests:
        return
    existing = container.get("data_tests")
    if not isinstance(existing, list):
        existing = CommentedSeq()
    # Fold dbt's deprecated `tests:` key into `data_tests:` — a container can't declare both, and
    # we're about to add ours. Only touches containers this sync writes tests into.
    legacy = container.pop("tests", None)
    if isinstance(legacy, list):
        existing.extend(legacy)
    for desired in desired_tests:
        test_name = _test_name(desired)
        if test_name is None:
            continue
        dc_check = _meta_block_for_test(desired).get("check")
        idx = next((i for i, entry in enumerate(existing) if _test_name(entry) == test_name), None)
        if idx is None:
            new_entry = CommentedMap(desired) if not isinstance(desired, CommentedMap) else desired
            _ensure_membership(
                new_entry, dc_check, generated=True, contract_version=contract_version, versioned=versioned
            )
            existing.append(new_entry)
        else:
            existing[idx] = _reconcile_test(existing[idx], desired, dc_check, contract_version, versioned=versioned)
    container["data_tests"] = existing


