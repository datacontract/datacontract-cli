def _is_odcs_file(path: Path) -> bool:
    """True if `path` is an ODCS data contract (`kind: DataContract`, `apiVersion: v3…`).

    `dbt sync` and `dbt test` are ODCS-only; a non-ODCS, empty, or unparseable file returns False so
    the caller can skip it rather than let the resolver silently coerce it into an empty contract.
    """
    try:
        data = yaml.safe_load(path.read_text(encoding="utf-8"))
    except Exception:
        return False
    return isinstance(data, dict) and is_open_data_contract_standard(data)


def _resolve_contract_paths(contracts: list[str], search_dir: Path) -> list[Path]:
    """Resolve requested contracts to a deduplicated, sorted list of paths.

    No tokens → discover every `*.odcs.yaml` under `search_dir`. Otherwise each token is a literal
    path (error if missing) or, when it contains glob metacharacters, a glob pattern (a pattern that
    matches nothing is silently ignored). Matches are unioned, deduplicated, and sorted.
    """
    if not contracts:
        candidates = sorted(p for p in search_dir.rglob("*.odcs.yaml") if p.is_file())
        if not candidates:
            raise DataContractException(
                type="dbt_sync",
                name="resolve contract",
                reason=(
                    f"No `*.odcs.yaml` found below {search_dir}.\n"
                    "  - Already have a contract? Pass its path: `datacontract dbt sync <contract>`.\n"
                    "  - Starting from this dbt project? Bootstrap a contract from it: run `dbt parse`, then "
                    "`datacontract import dbt --source target/manifest.json --output datacontract-v1.odcs.yaml`."
                ),
                engine="dbt-sync",
            )
        return candidates

    resolved: dict[Path, None] = {}
    for token in contracts:
        if any(ch in token for ch in "*?["):
            # A glob that matches nothing is silently ignored — globbing over optional locations is a
            # normal way to invoke sync; an empty match is not an error.
            matches = sorted(p.resolve() for p in (Path(m) for m in glob.glob(token, recursive=True)) if p.is_file())
            for m in matches:
                resolved[m] = None
        else:
            path = Path(token).resolve()
            if path.is_dir():
                raise DataContractException(
                    type="dbt_sync",
                    name="resolve contract",
                    reason=(
                        f"{path} is a directory, not a contract file. Pass --project-dir {path} "
                        "(and omit the contract argument) to sync every `*.odcs.yaml` under it."
                    ),
                    engine="dbt-sync",
                )
            if not path.is_file():
                raise DataContractException(
                    type="dbt_sync",
                    name="resolve contract",
                    reason=f"Contract file not found: {path}",
                    engine="dbt-sync",
                )
            resolved[path] = None
    return sorted(resolved)


def generate_dbt_tests(
    *,
    contract: str,
    project_dir: Optional[Path],
    schema_name: str = "all",
    model_resolution: ModelResolution = ModelResolution.name,
    prune: bool = False,
    model_version: Optional[str] = None,
    server: Optional[str] = None,
) -> DbtTestGenerationResult:
    """Resolve the contract, merge its tests + metadata into the dbt project's model YAML in place.

    `model_version` (a dbt model version like "2", from the contract filename) merges this contract
    into a versioned model — a `versions:` block — additively, leaving sibling versions untouched.
    `server` selects the ODCS server whose `type` maps `logicalType`s to a `data_type`.
    """
    explicit_project_dir = project_dir is not None
    project_dir = (project_dir or Path.cwd()).resolve()
    _ensure_dbt_project(project_dir, explicit=explicit_project_dir)

    contract_path = Path(contract).resolve()
    odcs = resolve_data_contract(str(contract_path))
    logger.info(f"Resolved contract {odcs.id}@{odcs.version} from {contract_path}")
    dialect = _resolve_type_dialect(odcs, server)

    run = Run.create_run()
    run.dataContractId = odcs.id
    run.dataContractVersion = odcs.version

    name_map = resolve_model_names(odcs, model_resolution, schema_name)
    if not name_map:
        run.log_info("No quality to sync (contract has no schemas with names).")

    model_paths, _ = _read_dbt_project_paths(project_dir)
    legacy_dir, tests_dir = _resolved_generated_dirs(project_dir)

    # Load every model-path YAML up front so model names can be matched case-insensitively
    # against the project's actual dbt models (#1254) before we generate anything.
    session = _EditSession()
    for path in _iter_model_yaml_files(model_paths, legacy_dir):
        session.try_load(path)

    # Resolve each schema to the project's actual dbt model name (case-preserved), then build
    # the desired state (model dict + singular SQL) against that name so `ref()` / patches match.
    schemas_by_name = {s.name: s for s in (odcs.schema_ or []) if s.name}
    plans: list[_ModelPlan] = []
    skipped_schemas: list[str] = []
    for schema_name_key, contract_model_name in name_map.items():
        effective, source, sql_path = _effective_model_name(session, model_paths, contract_model_name, model_version)
        if source == "missing":
            extra = f" (looked for `{contract_model_name}_v{model_version}.sql`)" if model_version is not None else ""
            run.log_warn(
                f"Schema `{schema_name_key}` resolves to model `{contract_model_name}`, which has no matching "
                f"dbt model (no `.sql` or YAML entry) in this project{extra} — nothing to test, skipping. "
                "If the model exists under a different name, try `--model-resolution physicalName`."
            )
            skipped_schemas.append(schema_name_key)
            continue
        # A non-versioned sync onto a versioned model (entry has a `versions:` block) can't merge safely:
        # it would strip the block and strand the `<model>_v*.sql` files.
        if model_version is None:
            located = _locate_entry(session, effective)
            versions = located[1].get("versions") if located is not None else None
            if isinstance(versions, list) and versions:
                raise DataContractException(
                    type="dbt_sync",
                    name="versioned model, unversioned contract",
                    reason=(
                        f"Model `{effective}` has a `versions:` block but contract `{odcs.id}` "
                        "was synced without a version. Name the contract file with the target version "
                        "(e.g. `<name>-v2.odcs.yaml`), or de-version the model's `.sql` files."
                    ),
                    engine="dbt-sync",
                )
        schema_obj = schemas_by_name[schema_name_key]
        model_dict, singulars = generate_dbt_tests_for_schema(odcs, schema_obj, effective, run, model_version)
        plans.append(_ModelPlan(effective, schema_obj, model_dict, singulars, sql_path))

    singular_tests: list[SingularTest] = [s for p in plans for s in p.singular_tests]

    # If there are unparseable yaml files, warn. In case we'd create a new yaml file, error and stop:
    # It could be that our new file would be a duplicate once the user fixes the bad yaml.
    if session.unparseable:
        files = ", ".join(str(p) for p in session.unparseable)
        if any(p.sql_path is not None for p in plans):
            raise DataContractException(
                type="dbt_sync",
                name="parse existing YAML files",
                reason=(f"Cannot parse YAML file(s): {files}\nFix or remove them before running dbt sync."),
                engine="dbt-sync",
            )
        run.log_warn(f"Ignoring unparseable YAML file: {files}")

    # Versioned sync skips the cross-model provenance cleanup (it can't tell which version a shared
    # test serves); `--prune` instead retires this-version-only footprint via `_prune_versioned_entry`.
    if model_version is None:
        required = RequiredState(
            scope_models={p.name.lower(): p.name for p in plans},
            required_tests={p.name.lower(): _required_test_keys(p.model_dict) for p in plans},
        )
        _run_yaml_cleanup(session, required, scope_contract_id=odcs.id)

    resolved_models: list[str] = []
    prunable: list[str] = []
    for plan in plans:
        located = _locate_entry(session, plan.name)
        if located is not None:
            _, entry = located
        else:
            # source == "sql": no YAML entry anywhere → new sidecar. A versioned model's schema lives
            # in one `<model>.yml` (its files are `<model>_v<N>.sql`); otherwise it's next to the `.sql`.
            if model_version is not None:
                sidecar_path = plan.sql_path.parent / f"{plan.name}.yml"
            else:
                sidecar_path = plan.sql_path.with_suffix(".yml")
            existing = session.files.get(sidecar_path)
            entry = CommentedMap()
            entry["name"] = plan.name
            if existing is not None:
                models = existing.data.get("models")
                if not isinstance(models, list):
                    models = CommentedSeq()
                    existing.data["models"] = models
                models.append(entry)
            else:
                session.create_sidecar(sidecar_path, entry)
        if model_version is not None:
            # Point the bullet at the actual file when it isn't dbt's default `<model>_v<N>.sql`.
            defined_in = (
                plan.sql_path.stem
                if plan.sql_path is not None and plan.sql_path.stem.lower() != f"{plan.name}_v{model_version}".lower()
                else None
            )
            _merge_versioned_model_entry(
                entry,
                plan.schema_obj,
                plan.model_dict,
                odcs,
                model_version,
                prune=prune,
                defined_in=defined_in,
                dialect=dialect,
                run=run,
            )
        else:
            _merge_model_entry(entry, plan.schema_obj, plan.model_dict, odcs, prune=prune, dialect=dialect, run=run)
        resolved_models.append(plan.name)
        if not prune:
            if model_version is None:
                items = _prunable_items(entry, plan.schema_obj, plan.model_dict)
            else:
                items = _prune_versioned_entry(entry, plan.model_dict, odcs.version or "no_version", apply=False)
            prunable.extend(f"{plan.name}: {item}" for item in items)

    # Singular SQL is CLI-owned, one `datacontract_cli/<id>/` subdir per contract, files named
    # `<id>__<version>__<...>.sql`. Regenerate only this version's own files; every other version's
    # are left in place (retiring a version is the user's job).
    _, tests_dir = _resolved_generated_dirs(project_dir)
    contract_subdir = tests_dir / _slugify(odcs.id or "contract")
    this_version = _slugify(odcs.version or "no_version")
    own_prefix = f"{contract_subdir.name}__{this_version}__"
    # Reconcile this version's own singular SQL: write only new/changed files and delete removed
    # ones, so a no-op re-sync reports nothing.
    desired = {contract_subdir / s.filename: s.sql for s in singular_tests}
    deleted_sql: list[Path] = []
    written_sql: list[Path] = []
    if contract_subdir.is_dir():
        for sql in contract_subdir.glob(f"{own_prefix}*.sql"):
            if sql not in desired:
                sql.unlink()
                deleted_sql.append(sql)
    for path, sql in desired.items():
        path.parent.mkdir(parents=True, exist_ok=True)
        if not path.exists() or path.read_text(encoding="utf-8") != sql:
            path.write_text(sql, encoding="utf-8")
            written_sql.append(path)

    # Migrate v1.0.9's flat layout: remove our top-level `*.sql` (the dir is CLI-owned wholesale).
    for stale in tests_dir.glob("*.sql"):
        stale.unlink()
        deleted_sql.append(stale)
    # Drop the subdir if this contract (and its resolved siblings) left it empty.
    if contract_subdir.is_dir() and not any(contract_subdir.iterdir()):
        contract_subdir.rmdir()
    _remove_legacy_dir(legacy_dir)
    written_yaml, deleted_yaml = session.apply()
    # Regenerated SQL is unlinked then rewritten; report only the ones not written back as deletions.
    written_sql_set = set(written_sql)
    deleted_files = [p for p in deleted_sql if p not in written_sql_set] + deleted_yaml

    return DbtTestGenerationResult(
        contract_path=contract_path,
        project_dir=project_dir,
        odcs=odcs,
        resolved_models=resolved_models,
        written_yaml=written_yaml,
        written_sql=written_sql,
        deleted_files=deleted_files,
        skipped_schemas=skipped_schemas,
        prunable=prunable,
        generation_run=run,
    )


def parse_dbt_test_run(
    project_dir: Path,
    odcs: OpenDataContractStandard,
    completed: subprocess.CompletedProcess,
    *,
    model_filter: Optional[set[str]] = None,
    generation_run: Optional[Run] = None,
) -> Run:
    """Turn an already-completed `dbt test` invocation into a `Run` for one contract.

    `model_filter` restricts the parsed checks to the contract's own models (multi-contract runs
    share a single `dbt test` invocation). `generation_run` is the `Run` from a same-invocation
    `generate_dbt_tests` (sync's `--run-tests` path); its logs and start time are stitched on.
    """
    parsed_run = parse_run_results_file(project_dir, odcs, model_filter=model_filter)
    ansi = re.compile(r"\x1b\[[0-9;]*[mGKHF]")
    for stream in (completed.stdout, completed.stderr):
        if not stream:
            continue
        for line in ansi.sub("", stream).splitlines():
            line = line.strip()
            if line:
                parsed_run.log_info(line)

    if generation_run is not None:
        # Stitch generation-time logs on so warn-counts include any generation-time skips.
        parsed_run.logs = generation_run.logs + parsed_run.logs
        parsed_run.timestampStart = generation_run.timestampStart
    parsed_run.finish()
    return parsed_run
